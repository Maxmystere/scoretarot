package fr.pssoftware.scoretarot;

public class Donne {
	private long id;
	private long id_partie;
	private int contrat;
	private int preneur;
	private int appele;
	private int mort;
	private int points;
	private int bouts;
	private int petit;
	private int poignee;
	private int chelem;
	
	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public long getId_partie() {
		return id_partie;
	}

	public void setId_partie(long id_partie) {
		this.id_partie = id_partie;
	}

	public int getContrat() {
		return contrat;
	}

	public void setContrat(int contrat) {
		this.contrat = contrat;
	}

	public int getPreneur() {
		return preneur;
	}

	public void setPreneur(int preneur) {
		this.preneur = preneur;
	}

	public int getAppele() {
		return appele;
	}

	public void setAppele(int appele) {
		this.appele = appele;
	}

	public int getMort() {
		return mort;
	}

	public void setMort(int mort) {
		this.mort = mort;
	}

	public int getPoints() {
		return points;
	}

	public void setPoints(int points) {
		this.points = points;
	}

	public int getBouts() {
		return bouts;
	}

	public void setBouts(int bouts) {
		this.bouts = bouts;
	}

	public int getPetit() {
		return petit;
	}

	public void setPetit(int petit) {
		this.petit = petit;
	}

	public int getPoignee() {
		return poignee;
	}

	public void setPoignee(int poignee) {
		this.poignee = poignee;
	}

	public int getChelem() {
		return chelem;
	}

	public void setChelem(int chelem) {
		this.chelem = chelem;
	}

	public Donne() {
	}

}
